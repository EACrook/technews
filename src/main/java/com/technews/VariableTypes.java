package com.technews;

public class VariableTypes {
    public static void main(String[] args) {
        int a = 15;
        double b = 3.14;
        String d = "Gus";
//  adds int and double
        double sum = a + b;
//  adds int and String
        String together = a + d;

        System.out.println(sum);
        System.out.println(together);
    }
}
